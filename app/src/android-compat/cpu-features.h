#pragma once

// Crypto++ 8.9 still includes the legacy NDK cpu-features header on Android.
// Modern NDKs no longer put it on the default include path. The Android build
// disables Crypto++ assembly/advanced ISA paths, so these stubs are only needed
// to let cpu.cpp parse cleanly.

#define ANDROID_CPU_FAMILY_UNKNOWN 0
#define ANDROID_CPU_FAMILY_ARM 1
#define ANDROID_CPU_FAMILY_X86 2
#define ANDROID_CPU_FAMILY_MIPS 3
#define ANDROID_CPU_FAMILY_ARM64 4
#define ANDROID_CPU_FAMILY_X86_64 5
#define ANDROID_CPU_FAMILY_MIPS64 6

#define ANDROID_CPU_ARM_FEATURE_ARMv7 0x00000001
#define ANDROID_CPU_ARM_FEATURE_VFPv3 0x00000002
#define ANDROID_CPU_ARM_FEATURE_NEON 0x00000004
#define ANDROID_CPU_ARM_FEATURE_LDREX_STREX 0x00000008
#define ANDROID_CPU_ARM_FEATURE_VFPv2 0x00000010
#define ANDROID_CPU_ARM_FEATURE_VFP_D32 0x00000020
#define ANDROID_CPU_ARM_FEATURE_VFP_FP16 0x00000040
#define ANDROID_CPU_ARM_FEATURE_VFP_FMA 0x00000080
#define ANDROID_CPU_ARM_FEATURE_NEON_FMA 0x00000100
#define ANDROID_CPU_ARM_FEATURE_IDIV_ARM 0x00000200
#define ANDROID_CPU_ARM_FEATURE_IDIV_THUMB2 0x00000400
#define ANDROID_CPU_ARM_FEATURE_iWMMXt 0x00000800
#define ANDROID_CPU_ARM_FEATURE_AES 0x00001000
#define ANDROID_CPU_ARM_FEATURE_PMULL 0x00002000
#define ANDROID_CPU_ARM_FEATURE_SHA1 0x00004000
#define ANDROID_CPU_ARM_FEATURE_SHA2 0x00008000
#define ANDROID_CPU_ARM_FEATURE_CRC32 0x00010000
#define ANDROID_CPU_ARM_FEATURE_SHA512 0x00020000
#define ANDROID_CPU_ARM_FEATURE_SHA3 0x00040000
#define ANDROID_CPU_ARM_FEATURE_SM3 0x00080000
#define ANDROID_CPU_ARM_FEATURE_SM4 0x00100000

#define ANDROID_CPU_ARM64_FEATURE_FP 0x00000001
#define ANDROID_CPU_ARM64_FEATURE_ASIMD 0x00000002
#define ANDROID_CPU_ARM64_FEATURE_AES 0x00000004
#define ANDROID_CPU_ARM64_FEATURE_PMULL 0x00000008
#define ANDROID_CPU_ARM64_FEATURE_SHA1 0x00000010
#define ANDROID_CPU_ARM64_FEATURE_SHA2 0x00000020
#define ANDROID_CPU_ARM64_FEATURE_CRC32 0x00000040
#define ANDROID_CPU_ARM64_FEATURE_SHA512 0x00000080
#define ANDROID_CPU_ARM64_FEATURE_SHA3 0x00000100
#define ANDROID_CPU_ARM64_FEATURE_SM3 0x00000200
#define ANDROID_CPU_ARM64_FEATURE_SM4 0x00000400

static inline int android_getCpuFamily(void)
{
    return ANDROID_CPU_FAMILY_UNKNOWN;
}

static inline unsigned long long android_getCpuFeatures(void)
{
    return 0;
}
